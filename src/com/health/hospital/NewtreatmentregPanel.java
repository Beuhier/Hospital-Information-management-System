    private void newtreatmentregCardNotextfieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_newtreatmentregCardNotextfieldFocusLost
        // TODO add your handling code here:
        String card = newtreatmentregCardNotextfield.getText();
        if (!card.isEmpty())
        {
            DBoperations db = new DBoperations();
            boolean reply = db.cardValid(card);
            
            if(reply)
            {
                newpatientregcardsignLabel.setIcon(new javax.swing.ImageIcon("C:\\Users\\chinex\\Documents\\NetBeansProjects\\goodsign.png"));
                newpatientregcardwarningLabel.setText(" ");
            }
            else
            {
                 newpatientregcardsignLabel.setIcon(new javax.swing.ImageIcon("C:\\Users\\chinex\\Documents\\NetBeansProjects\\badsign.png"));
                 newpatientregcardwarningLabel.setText("Invalid Card Number,Please click link to get registered");
            }
        }
    }//GEN-LAST:event_newtreatmentregCardNotextfieldFocusLost

