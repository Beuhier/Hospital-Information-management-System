    private void newtreatmentregSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newtreatmentregSaveButtonActionPerformed
        // TODO add your handling code here:
      
        
       String cardNo    =    newtreatmentregCardNotextfield.getText();
       String fullname  =    newtreatmentregpatientnametextfield.getText();
       Date startDate = newtreatmentregDateChoosertextfield.getDate();
       Date dob =  newtreatmentregdobDateChooser.getDate();
       String patsym = newtreatmentregpatientsymptontextarea.getText();
       
       SimpleDateFormat ft = new SimpleDateFormat ("yyyy-MM-dd");
       
       String startDateStr = ft.format(startDate);
       String dobStr = ft.format(dob);
       
       DBoperations db = new DBoperations();
       int cardCount =  db.getCardNoCount(cardNo);
       cardCount++;
       
       String CardId = cardNo.concat("_" + cardCount);
       String reply = db.saveOngoingTreatmentRecord(CardId, cardNo, fullname, startDateStr, dobStr, patsym);
       if (reply.equals("success"))
       {
           HashMap<String, String[]> objMap = db.getAllTreatmentRecordByCardNo(cardNo);
           TabularcardRecordPanel tbPanel = new TabularcardRecordPanel(objMap,cardNo);
           loginFrame.updatePanel(tbPanel);
       }
       else
       {
           
       }
       newtreatmentregCardNotextfield.setText("");
       newtreatmentregpatientnametextfield.setText("");
       newtreatmentregDateChoosertextfield.setDate(null);
       newtreatmentregdobDateChooser.setDate(null);
       newtreatmentregpatientsymptontextarea.setText(""); 
    }//GEN-LAST:event_newtreatmentregSaveButtonActionPerformed

